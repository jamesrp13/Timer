import UIKit

extension UIColor {
    
    static func customLightBlue() -> UIColor {
        return UIColor(red: CGFloat(64.0/255.0), green: CGFloat(180.0/255.0), blue: CGFloat(229.0/255.0), alpha: 1.0)
    }
    
    static func customDarkBlue() -> UIColor {
        return UIColor(red: 0.0, green: CGFloat(92.0/255.0), blue: CGFloat(185.0/255.0), alpha: 1.0)
    }
    
    static func customDarkGrey() -> UIColor {
        return UIColor(red: CGFloat(92.0/255.0), green: CGFloat(102.0/255.0), blue: CGFloat(112.0/255.0), alpha: 1.0)
    }
    
    static func customDarkYellow() -> UIColor {
        return UIColor(red: CGFloat(255.0/255.0), green: CGFloat(198.0/255.0), blue: CGFloat(39.0/255.0), alpha: 1.0)
    }
    
    static func customBrown() -> UIColor {
        return UIColor(red: CGFloat(184.0/255.0), green: CGFloat(132.0/255.0), blue: 0.0, alpha: 1.0)
    }
    
}


